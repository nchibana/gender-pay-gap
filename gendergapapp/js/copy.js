const filterOccupationsOnChange = (event) => {
  const value = event.target.value.toLowerCase();

  const result = occupationsResult.filter((occupationObject) => {
    const occupation = occupationObject.occupation || '';
    const occupationString = `${occupation}`;

    return occupationString.toLowerCase().includes(value);
  });
  
  generateOccupationsList(result);
};

const addEventListenerToOccupations = () => {
  const occupationsInput = document.querySelector('.occupations-input');
  const occupationListItems = document.querySelectorAll('.occupation');
  let occupationListItemIndex = 0;
  const occupationListItemLength = occupationListItems.length;

  for (occupationListItemIndex; occupationListItemIndex < occupationListItemLength; occupationListItemIndex++) {
    const occupationListItem = occupationListItems[occupationListItemIndex];

    occupationListItem.addEventListener('mousedown', (event) => {
      event.preventDefault();
      event.stopPropagation();

      const value = event.currentTarget.innerText;
      occupationsInput.value = value;

      filterOccupationsOnChange({ target: { value } });

      occupationsInput.focus();
    });
  }
}

const createOccupationListItem = ({ occupation = '' }) => (
  `<li class="occupation" data-value="${occupation}"><span class="occupation--name">${occupation}</span></li>`
);

const generateOccupationsList = (occupationsArray) => {
  const occupationsList = document.querySelector('.occupations-list');
  occupationsList.innerHTML = '';
  
  const occupationsLength = occupationsArray.length;

  for (let occupationIndex = 0; occupationIndex < occupationsLength; occupationIndex++) {
    occupationsList.innerHTML += createOccupationListItem(occupationsArray[occupationIndex]);
  }
  
  addEventListenerToOccupations();
};

document.addEventListener("DOMContentLoaded", () =>{
  const occupationsInput = document.querySelector('.occupations-input');

  generateOccupationsList(occupationsResult);
  
  occupationsInput.addEventListener('keyup', filterOccupationsOnChange);

  occupationsInput.addEventListener('focus', () => {
    const occupationsList = document.querySelector('.occupations-list-container');
    occupationsList.classList.add('visible');
  });

  occupationsInput.addEventListener('blur', (event) => {
    const occupationsList = document.querySelector('.occupations-list-container');
    occupationsList.classList.remove('visible');
  });

});